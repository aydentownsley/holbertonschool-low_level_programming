/**
 * add - adds two ints
 *
 * @a: int 1
 * @b: int 2
 *
 * Return: a + b
 */
int add(a, b)
{
	return (a + b);
}

/**
 * sub - subtracts two ints
 *
 * @a: int 1
 * @b: int 2
 *
 * Return: a - b
 */
int sub(a, b)
{
        return (a - b);
}

/**
 * mul - multiplies two ints
 *
 * @a: int 1
 * @b: int 2
 *
 * Return: a * b
 */
int mul(a, b)
{
        return (a * b);
}

/**
 * div - divides two ints
 *
 * @a: int 1
 * @b: int 2
 *
 * Return: a / b
 */
int div(a, b)
{
        return (a / b);
}

/**
 * mod - modulos two ints
 *
 * @a: int 1
 * @b: int 2
 *
 * Return: a % b
 */
int mod(a, b)
{
        return (a % b);
}
